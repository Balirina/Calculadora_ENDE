package interfazCLI.com;


/**
 * @author nereeea
 * Clase con los metodos sumar, restar y multiplicar
 *
 */
public class Sumar_restar_multiplicar {
	
	/**
	@Metodo que suma dos numeros
	@param a
	@param b
	@return resultado
	@author nereeea
	
	*/
	public static double sumar(double a, double b)
	{
		 return a+b;
	}
	
	/**
	 * Metodo que resta dos numeros
	 * @param a
	 * @param b
	 * @return resultado
	 * @author nereeea
	 */
	
	public static double restar(double a, double b)
	{
		return a-b;
	}

	/**
	 * Metodo que multiplica dos numeros
	 * @param a
	 * @param b
	 * @return resultado
	 * @author nereeea
	*/
	public static double  multiplicar(double a, double b)
	{
		return a*b;
	}


}
